mae = int(input("Digite a idade da mãe: "))
pai = int(input("Digite a idade do pai: "))
filho = int(input("Digite a idade do filho: "))

dif_m_f = mae - filho
print(f"A mãe é {dif_m_f} anos mais velha que o filho.")

dif_p_f = pai - filho
print(f"O pai é {dif_p_f} anos mais velho que o filho.")

dif_p_m = pai - mae
print(f"A diferença de idade entre o pai e a mãe é de {dif_p_m} anos.")   